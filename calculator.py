#Addition Operation
def myAdd(num1,num2):
    return num1+num2

#Substraction Operation
def mySub(num1,num2):
    return num1-num2

#Multiplication Operation
def myMul(num1,num2):
    return num1*num2

#Division Operation
def myDiv(num1,num2):
    return num1/num2

result1=myAdd(20,30)
result2=mySub(60,20)
result3=myMul(50,50)
result4=myDiv(10,2)

print(result1)
print(result2)
print(result3)
print(result4)
